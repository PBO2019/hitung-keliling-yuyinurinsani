public class Kelilinglingkaran {
   private double PI=3.14 ;
   private double jari=5 ;
   private double kll ;

   public void hitungkeliling () {
       this.kll = 2* PI *  jari ;
   }

   public void setPI (double PI){
       this.PI = PI ;
   }

   public void setjari (double jari){
       this.jari= jari;
    }

    public double getkll () {
       return kll;
    }
    }



